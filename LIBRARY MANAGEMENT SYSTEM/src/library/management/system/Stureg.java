    private void SAVEBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAVEBTActionPerformed
       if(txtid.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtid.requestFocus();
    }//GEN-LAST:event_SAVEBTActionPerformed
       else if(txtname.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtname.requestFocus();
    }  
       else if(txtbranch.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtbranch.requestFocus();
    }  
       else if(txtcourse.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtcourse.requestFocus();
    }  
       else if(txtsemester.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtsemester.requestFocus();
    } 
       else{
           PreparedStatement pst=null;
           try {
               Connection con=Connect.Connection();
               pst = con.prepareStatement("INSERT INTO STUDENT(id,name,course,branch,semester)values(?,?,?,?,?)");
               pst.setString(1,txtid.getText());
               pst.setString(2,txtname.getText());
               pst.setString(3,txtcourse.getText());
               pst.setString(4,txtbranch.getText());
               pst.setString(5,txtsemester.getText());
               pst.executeUpdate();
               JOptionPane.showMessageDialog(this,"RECORD SAVED");
               txtid.setText("");
               txtname.setText("");
               txtcourse.setText("");
               txtbranch.setText("");
               txtsemester.setText("");
           } catch (SQLException ex) {
               Logger.getLogger(Stureg.class.getName()).log(Level.SEVERE, null, ex);
           } finally {
               try {
                   pst.close();
               } catch (SQLException ex) {
                   Logger.getLogger(Stureg.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
       }
    }
