    private void savebookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebookActionPerformed
        if(txtbid.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtbid.requestFocus();
    }                                      
       else if(txtbname.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtbname.requestFocus();
    }  
       else if(txtbpub.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtbpub.requestFocus();
    }  
       else if(txtbprice.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtbprice.requestFocus();
    }  
       else if(txtpubyear.getText().equals("")){
           JOptionPane.showMessageDialog(this, "ALL FIELDS REQUIRED");
           txtpubyear.requestFocus();
    } 
       else{
           PreparedStatement pst=null;
           try {
               Connection con=Connect.Connection();
               pst = con.prepareStatement("INSERT INTO BOOK(id,bookname,publisher ,price,year)values(?,?,?,?,?)");
               pst.setString(1,txtbid.getText());
               pst.setString(2,txtbname.getText());
               pst.setString(3,txtbpub.getText());
               pst.setString(4,txtbprice.getText());
               pst.setString(5,txtpubyear.getText());
               pst.executeUpdate();
               JOptionPane.showMessageDialog(this,"RECORD SAVED");
               txtbid.setText("");
               txtbname.setText("");
               txtbpub.setText("");
               txtbprice.setText("");
               txtpubyear.setText("");
           } catch (SQLException ex) {
               Logger.getLogger(Stureg.class.getName()).log(Level.SEVERE, null, ex);
           } finally {
               try {
                   pst.close();
               } catch (SQLException ex) {
                   Logger.getLogger(Stureg.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
       }
    }//GEN-LAST:event_savebookActionPerformed

